package com.bank.trade;

public class TradeDTO {
	private int tno, tono, tipno, tmoney;
	private String ttime, toaccount, tipaccount, tip;
	public int getTno() {
		return tno;
	}
	public void setTno(int tno) {
		this.tno = tno;
	}
	public int getTono() {
		return tono;
	}
	public void setTono(int tono) {
		this.tono = tono;
	}
	public int getTipno() {
		return tipno;
	}
	public void setTipno(int tipno) {
		this.tipno = tipno;
	}
	public int getTmoney() {
		return tmoney;
	}
	public void setTmoney(int tmoney) {
		this.tmoney = tmoney;
	}
	public String getTtime() {
		return ttime;
	}
	public void setTtime(String ttime) {
		this.ttime = ttime;
	}
	public String getToaccount() {
		return toaccount;
	}
	public void setToaccount(String toaccount) {
		this.toaccount = toaccount;
	}
	public String getTipaccount() {
		return tipaccount;
	}
	public void setTipaccount(String tipaccount) {
		this.tipaccount = tipaccount;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
}
